<script setup>
import { ref, onMounted } from 'vue'

// dynamic year shown in template
const year = ref(new Date().getFullYear())

// default social links - update to your real pages
const socials = [
    { label: 'Github', url: 'https://github.com/' },
    { label: 'LinkedIn', url: 'https://www.linkedin.com/' },
    { label: 'Twitter', url: 'https://twitter.com/' },
    { label: 'Facebook', url: 'https://www.facebook.com/' }
]

onMounted(() => {
    // wire up social link icons with the social links by matching their title attribute
    document.querySelectorAll('.social-link').forEach(el => {
        const title = el.getAttribute('title')
        const match = socials.find(s => s.label.toLowerCase() === title.toLowerCase())
        if (match) {
            el.setAttribute('href', match.url)
            el.setAttribute('target', '_blank')
            el.setAttribute('rel', 'noopener noreferrer')
            el.addEventListener('click', (e) => {
                el.classList.add('clicked')
                setTimeout(() => el.classList.remove('clicked'), 280)
            })
        }
    })

    // add smooth scroll behavior for footer links
    document.querySelectorAll('.footer-link').forEach(link => {
        link.addEventListener('click', (e) => {
            const href = link.getAttribute('href')
            if (href === '#') {
                e.preventDefault()
            }
        })
    })
})
</script>

