<script setup>
import { ref, reactive } from 'vue';
import axios from 'axios';

const form = ref({
    full_name: '',
    email: '',
    programme: '',
    phone: '',
    message: ''
});

const isSubmittingSuccess = ref(false);
const submitMessage = ref('');
const submitError = ref('');
const isSubmitting = ref(false);

const submitForm = async () => {
    isSubmitting.value = true;
    submitMessage.value = '';
    submitError.value = '';
    try {
        const response = await axios.post('http://localhost:3000/contacts', form.value);
        submitMessage.value = 'Your message was sent successfully!';
        isSubmittingSuccess.value = true;
        form.value = {
            full_name: '',
            email: '',
            programme: '',
            phone: '',
            message: ''
        }
        // clear Forms fields in 3 seconds
        setTimeout(() => {
            isSubmittingSuccess.value = false;
        }, 3000);
    } catch (error) {
        submitError.value = 'There was an error sending your message, please try again later';
    } finally {
        isSubmitting.value = false;
    }
}
</script>

